    <script>
        const canvas = document.getElementById('signature-canvas');
        const context = canvas.getContext('2d');
        let isDrawing = false;

        function setCanvasBackground() {
            context.fillStyle = "white";
            context.fillRect(0, 0, canvas.width, canvas.height);
        }
        setCanvasBackground();
        canvas.addEventListener('mousedown', (event) => {
            isDrawing = true;
            context.beginPath();
            context.moveTo(event.offsetX, event.offsetY);
        });

        canvas.addEventListener('mousemove', (event) => {
            if (isDrawing) {
                context.lineTo(event.offsetX, event.offsetY);
                context.strokeStyle = "black";
                context.lineWidth = 2;
                context.stroke();
            }
        });

        canvas.addEventListener('mouseup', () => {
            isDrawing = false;
        });

        canvas.addEventListener('mouseleave', () => {
            isDrawing = false;
        });

        document.getElementById('clear-canvas').addEventListener('click', (e) => {
            e.preventDefault();
            context.clearRect(0, 0, canvas.width, canvas.height);
            setCanvasBackground();
        });
    </script>
    <script>
        document.addEventListener("DOMContentLoaded", function() {
            const webcamModal = new bootstrap.Modal(document.getElementById('webcamModal'));
            const webcamVideo = document.getElementById('webcam');
            const photoCanvas = document.getElementById('photoCanvas');
            const photoContext = photoCanvas.getContext('2d');
            const photoPreview = document.getElementById('photoPreview');

            document.getElementById('take-picture').addEventListener('click', function(e) {
                e.preventDefault();
                webcamModal.show();
                navigator.mediaDevices.getUserMedia({
                        video: true
                    })
                    .then(function(stream) {
                        webcamVideo.srcObject = stream;
